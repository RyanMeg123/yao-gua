const getOpenRouterClient = () => {
  const apiKey = process.env.OPENROUTER_API_KEY;
  if (!apiKey) {
    throw new Error('OPENROUTER_API_KEY is not set');
  }
  return new OpenAI({
    apiKey,
    baseURL: 'https://openrouter.ai/api/v1',
  });
};

export async function POST(request: Request) {
  try {
    const { userInfo, tosses } = await request.json();

    const client = getOpenRouterClient();

    const prompt = `
用户起卦信息如下：
- 摇卦时间：${userInfo.time}
- 所问之事：${userInfo.question}
- 问卦人：${userInfo.age} 岁 ${userInfo.gender}
- 6次硬币结果（从初爻到上爻）：
${tosses.map((t: any, i: number) => `第${i + 1}次: ${t.coins.map((c: string) => (c === 'H' ? '正' : '反')).join('')} (${t.yaoType}${t.isChanging ? ', 变爻' : ''})`).join('\n')}

请根据以上信息，按照系统指令的要求进行深度解卦。
`;

    const completion = await client.chat.completions.create({
      model: 'anthropic/claude-sonnet-4-5',
      messages: [
        { role: 'system', content: SYSTEM_PROMPT },
        { role: 'user', content: prompt },
      ],
      temperature: 0.7,
    });

    return NextResponse.json({ interpretation: completion.choices[0].message.content });
  } catch (error: any) {
    console.error('Error detail:', JSON.stringify({
      message: error.message,
      status: error.status,
      code: error.code,
      cause: error.cause?.message,
    }));
    return NextResponse.json(
      { error: error.message || 'Failed to get interpretation' },
      { status: 500 }
    );
  }
}

